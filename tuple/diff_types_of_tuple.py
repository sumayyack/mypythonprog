## different types of tuple

# empty tuple
my_tuple =()
print(my_tuple)

# tuple having integers
my_tuple =(1,2,3)
print(my_tuple)

# tuple with mixed datatypes
my_tuple =(1,"hello",3.4)
print(my_tuple)


# nested tuple
my_tuple= ("mouse",[8,4,6],(1,2,3))
print(my_tuple)


# tuple with one element
a=2,
print(a)
print(type(a))