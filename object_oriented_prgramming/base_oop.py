# # # OOP object oriented programming
# clas.....design pattern
# objects......real world entity
# reference........operations

#class
class Person:
    def walk(self):    # self_instance keyword
        print("person is walking")
    def read(self):
        print("person is reading")

#object
pe1=Person()
pe1.walk()
pe1.read()


#object2
pe2=Person()
pe2.walk()
pe2.read()